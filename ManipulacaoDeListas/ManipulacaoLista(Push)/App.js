// Criar um array vazio
let minhaArray = [];

// Adicionar elementos ao array usando o método push
minhaArray.push(10);       // Adiciona o número 10 ao final do array
minhaArray.push("Olá");   // Adiciona a string "Olá" ao final do array
minhaArray.push(true);    // Adiciona o valor booleano true ao final do array

// Exibir o array resultante no console
console.log(minhaArray); // Saída: [10, "Olá", true]