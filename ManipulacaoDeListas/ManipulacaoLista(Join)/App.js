// Criando um array de exemplo
const arrayExemplo = ["Maçã", "Banana", "Pera", "Uva"];

// Usando o método join() para unir os elementos do array em uma única string
const stringUnida = arrayExemplo.join(" - "); // Você pode especificar o separador desejado

// Exibindo a string unida
console.log("Array original:", arrayExemplo);
console.log("String unida:", stringUnida);