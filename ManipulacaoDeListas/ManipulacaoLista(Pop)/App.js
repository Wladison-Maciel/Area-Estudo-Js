// Criando uma lista (array) com alguns elementos
const minhaLista = [1, 2, 3, 4, 5];

// Utilizando o método pop() para remover o último elemento
let elementoRemovido = minhaLista.pop();

// Exibindo o elemento removido e a lista atualizada
console.log("Elemento removido: " + elementoRemovido);
console.log("Lista atualizada: " + minhaLista);